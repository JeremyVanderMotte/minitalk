#include "ft_printf.h"

int	ft_nb_len(long long nbr)
{
	int					ret;

	ret = 1;
	if (nbr < 0)
	{
		ret++;
		nbr = -nbr;
	}
	while (nbr > 9)
	{
		ret++;
		nbr /= 10;
	}
	return (ret);
}
